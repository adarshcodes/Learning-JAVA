package operators;

public class bitwise_operators {
   public static void main (String [] args) {
	   // Based on Gates
	   int a=10;
	   int b= 13;
	   int c= a & b;
	   int d= a | b;
	   int e= a>>b;
	   int f= a<<b;
	   System.out.println(e);
	   System.out.println(f);
	   System.out.println(d);
	   System.out.println(c);
	   
	   
   }
}
